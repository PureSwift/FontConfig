/// Font Width
public enum FontWidth: Int32, Sendable {
    case ultraCondensed = 50
    case extraCondensed = 63
    case condensed = 75
    case semiCondensed = 87
    case normal = 100
    case semiExpanded = 113
    case expanded = 125
    case extraExpanded = 150
    case ultraExpanded = 200
}
